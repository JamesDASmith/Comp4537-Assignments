export class Math
{
    /**
     * Adds two numbers
     * @param {*} a 
     * @param {*} b 
     * @returns 
     */
    static add(a,b) {
        return a + b;
    }

    /**
     * Subtracts two numbers
     * @param {*} a 
     * @param {*} b 
     * @returns 
     */
    static subtract(a,b) {
        return a - b;
    }
}